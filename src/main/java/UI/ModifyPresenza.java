    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {

            PresenzaDAO dao = null;
            DBConnection dbconn = null;

            int response = JOptionPane.showConfirmDialog(this, "Vuoi modificare questi dati all'interno del db?", "Conferma", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (response == JOptionPane.YES_OPTION) {

                dbconn = DBConnection.getInstance();
                Connection connection = dbconn.getConnection();
                dao = new PresenzaDAOPostgresImpl(connection);
                SimpleDateFormat c = new SimpleDateFormat("yyyy-MM-dd");
                SimpleDateFormat format = new SimpleDateFormat("HH:mm");

                java.sql.Time timeValue = new java.sql.Time(format.parse(jTextField2.getText()).getTime());
                java.sql.Time timeValueEND = new java.sql.Time(format.parse(jTextField3.getText()).getTime());

                Presenza p1 = new Presenza(c.parse(jTextField4.getText()), timeValue, timeValueEND, jTextField1.getText(), jTextField5.getText(), jTextField6.getText());

                int res = dao.UpdatePresenza(p1);
                System.out.println(res);
            } else if (response == JOptionPane.NO_OPTION || response == JOptionPane.CLOSED_OPTION) {
                System.out.println(" no insert done ");
            }
        } catch (SQLException | ParseException ex) {
            java.util.logging.Logger.getLogger(ModifyPresenza.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        PresenzaDAO dao = null;
        DBConnection dbconn = null;


        int response = JOptionPane.showConfirmDialog(this, "Vuoi cancellare questi dati all'interno del db?", "Conferma", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (response == JOptionPane.YES_OPTION) {
            try {
                dbconn = DBConnection.getInstance();
                Connection connection = dbconn.getConnection();
                dao = new PresenzaDAOPostgresImpl(connection);
                PresenzaDAOPostgresImpl ps = new PresenzaDAOPostgresImpl(connection);
                ps.deletePresenzaPS.setString(1, jTextField5.getText());
                ResultSet rs = ps.deletePresenzaPS.executeQuery();

            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(ModifyPresenza.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (response == JOptionPane.NO_OPTION || response == JOptionPane.CLOSED_OPTION) {
            System.out.println(" no operation done ");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

