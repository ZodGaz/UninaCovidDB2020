    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String uniqueID = UUID.randomUUID().toString();
        String descrizione = "";
        LuoghiDAO dao = null;
        DBConnection dbconn = null;

        int response = JOptionPane.showConfirmDialog(this, "Vuoi inserire questi dati all'interno del db?Verr√† generato un codice univoco per identificare il luogo", "Conferma", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (response == JOptionPane.YES_OPTION) {

            try {
                dbconn = DBConnection.getInstance();
                Connection connection = dbconn.getConnection();
                dao = new LuoghiDAOPostgresImpl(connection);
                descrizione = jTextField1.getText() +" "+ jTextField3.getText() +","+ jTextField2.getText() +","+ jTextField4.getText();
                Luoghi l1 = new Luoghi(descrizione,uniqueID);

                int res = dao.inserisciLuoghi(l1);
                JOptionPane.showMessageDialog(this, "L'id associato a "+descrizione+" √® "+"-->"+uniqueID);
                System.out.println(res);
            } catch (SQLException ex) {
                Logger.getLogger(LocationMenu.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else if (response == JOptionPane.NO_OPTION || response == JOptionPane.CLOSED_OPTION) {
            System.out.println("UI.ContactMenu.jButton2ActionPerformed()-> no insert done ");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

